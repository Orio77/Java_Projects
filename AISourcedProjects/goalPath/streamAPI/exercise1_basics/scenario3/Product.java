package streamAPI.exercise1_basics.scenario3;

public class Product {
    private String name;
    private String category;
    private int price;
    private int sales;

    public String getCategory() {
        return category;
    }
    public String getName() {
        return name;
    }
    public int getPrice() {
        return price;
    }
    public int getSales() {
        return sales;
    }
}
