package Basic_1.Ex32;

public class CompareNumbers {
    public static void printComparsion(int number_1, int number_2) {

        if (number_1 == number_2)
            System.out.println(number_1 + " = " + number_2);
        if (number_1 != number_2)
            System.out.println(number_1 + " != " + number_2);
        if (number_1 >= number_2)
            System.out.println(number_1 + " >= " + number_2);
        if (number_1 <= number_2)
            System.out.println(number_1 + " <= " + number_2);
        if (number_1 < number_2)
            System.out.println(number_1 + " < " + number_2);
        if (number_1 > number_2)
            System.out.println(number_1 + " > " + number_2);
    }
}
